#include "libft.h"

static int	count_words(const char *str, char c)
{
	int	i;
	int	trigger;

	i = 0;
	trigger = 0;
	while (*str)
	{
		if (*str != c && trigger == 0)
		{
			trigger = 1;
			i++;
		}
		else if (*str == c)
			trigger = 0;
		str++;
	}
	return (i);
}

char	**clear_splitted(char **tab)
{
	unsigned int	i;

	i = 0;
	while (tab[i])
		free(tab[i++]);
	free(tab);
	return (NULL);
}

char	**ft_split(char const *s, char c)
{
	size_t	i;
	size_t	j;
	int		index;
	char	**split;

	split = (char**)malloc((count_words(s, c) + 1 * sizeof(char *)));
	if (!s || !split)
		return (0);
	i = 0;
	j = 0;
	index = -1;
	while (i <= ft_strlen(s))
	{
		if (s[i] != c && index < 0)
			index = i;
		else if ((s[i] == c || i == ft_strlen(s)) && index >= 0)
		{
			if (!(split[j] = malloc(sizeof(char) * (i - index + 1))))
				return (clear_splitted(split));
			ft_strlcpy(split[j], s + index, i - index + 1);
			//split[j] = ft_substr(s, index, i - index);
			index = -1;
			j++;
		}
		i++;
	}
	split[j] = NULL;
	return (split);
}
